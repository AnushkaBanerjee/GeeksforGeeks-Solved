class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int n = arr.size();
        int maxEl = -1;
        int sMaxEl = -1;
        for(auto it:arr){//12 35 1 10 34 1
            if(it > maxEl){
                sMaxEl = maxEl;
                maxEl = it;
            }else if(sMaxEl < it && it != maxEl){
                sMaxEl = it;
            }
        }
        return sMaxEl;
    }
};

